import withNuxt from './.nuxt/eslint.config.mjs'
import tseslint from 'typescript-eslint'
import vueParser from 'vue-eslint-parser'

/**
 * ESLint flat config for the Tsundoku frontend.
 *
 * Layers, in order:
 *   1. The Nuxt-generated base (`withNuxt`): Vue SFC parsing, Nuxt auto-import
 *      globals, and the recommended Vue + TypeScript (non type-aware) rules.
 *   2. typescript-eslint's *type-aware* rule sets — these read the real types,
 *      so they catch passing a string where a number is expected, unhandled
 *      `null`/`undefined`, floating promises, etc.
 *   3. A final block that RE-ASSERTS the Vue parser for `.vue` files: the
 *      type-aware configs above set the parser to typescript-eslint's parser for
 *      every file, which would break `<template>` parsing — so SFCs are pointed
 *      back at vue-eslint-parser (which delegates `<script>` to the TS parser)
 *      and given `projectService` + `extraFileExtensions` so they still get type
 *      information.
 *
 * The real type *guarantee* (string-vs-number, null safety across `.vue`) comes
 * from `vue-tsc` via `bun run typecheck`; this config is the lint layer on top.
 */
export default withNuxt(
  {
    // schema.d.ts is generated by openapi-typescript (`bun run gen:api`) and is
    // never hand-edited (QCAT-021) — exempt it from lint so generator output
    // (index signatures etc.) doesn't fail the gate. The hand-written wrapper
    // client.ts is still linted.
    ignores: ['app/utils/api/schema.d.ts'],
  },
  ...tseslint.configs.recommendedTypeChecked,
  ...tseslint.configs.stylisticTypeChecked,
  {
    languageOptions: {
      parserOptions: {
        // `allowDefaultProject` lets root/tooling config files be linted even
        // though they sit outside the Nuxt tsconfig project.
        projectService: { allowDefaultProject: ['eslint.config.mjs', '.storybook/main.ts', '.storybook/preview.ts'] },
        tsconfigRootDir: import.meta.dirname,
      },
    },
  },
  {
    files: ['**/*.vue'],
    languageOptions: {
      parser: vueParser,
      parserOptions: {
        parser: tseslint.parser,
        projectService: true,
        extraFileExtensions: ['.vue'],
        tsconfigRootDir: import.meta.dirname,
      },
    },
  },
  {
    rules: {
      // In <script setup lang="ts"> an optional prop is written `prop?: T` and
      // TypeScript already models its possibly-undefined value. Requiring a
      // runtime `default` on every optional prop is redundant here and actively
      // wrong for pass-through props. Concrete fallbacks are still declared with
      // `withDefaults` wherever one is actually intended.
      'vue/require-default-prop': 'off',
    },
  },
  {
    // Tooling config files (this file + Storybook setup) are linted with the
    // default project, where imported configs resolve to `any`. Turn off the
    // type-aware rules for them — they're build config, not shipped app code.
    files: ['eslint.config.mjs', '.storybook/**'],
    ...tseslint.configs.disableTypeChecked,
  },
)
